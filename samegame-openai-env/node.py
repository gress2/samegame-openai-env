class Node:

  def __init__(self):
    self.q = None
    self.n = 0
    self.children = list()

  def get_q():
    return self.q

  def set_q(value):
    self.q = value

  def get_n():
    return self.n

  def set_n(value):
    self.n = value

  def get_child(i):
    return self.children[i]

  def get_children():
    return self.children

  def num_children():
    return len(self.children)